#include<iostream>
using namespace std;
int main()
{
    cout<<(i++)<<endl;        //this is post-increment operator
    cout<<(++i)<<endl;        //this is pre-increment operator
    cout<<(i--)<<endl;        //this is post-decrement operator
    cout<<(--i)<<endl;        //this is pre-decrement operator
}




