// here is the first code of "for" loop...
#include<iostream>
using namespace std;
int main() {
  int n;
  cout<<"enter a number: "<<endl;
  cin>>n;
  cout<<"counting numbers from 1 to n: ";
  for(int i=1;i<=n;i++) {
      cout<<i<<endl;
  }
}





